"""
04d_train_player_context_minute.py
====================================
Full minute-level player-context architecture + CPU/GPU benchmarking.

Architecture
------------

  Level 1 — Within-game encoder  (shared weights)
    Input : one player's per-minute timeline sequence for a SINGLE past game
            shape (T_game, per_player_feat_dim)
    Model : small causal Transformer  (d=64, nhead=2, layers=2)
    Output: game_embedding = last real token  (dim=64)

  Level 2 — Cross-game encoder  (shared weights)
    Input : K game_embeddings for one player, ordered chronologically
            shape (K, 64)
    Model : non-causal Transformer (all history is already past)
            → mean-pool over K outputs
    Output: player_embedding  (dim=64)

  Level 3 — Game predictor
    Input : [player_token_0..9 | game_frame_0..T-1]
            Uses the same hybrid causal mask as 04c.
    Output: win_prob per game frame

Data requirements
-----------------
  data/processed/features.parquet              — per-minute game features
  data/processed/player_game_summary.parquet   — game-level player index
  data/processed/player_minute_sequences.parquet  (needed for full run)
      generated by: python src/03b_build_player_index.py --include-sequences

Benchmarking
------------
    --benchmark flag: runs a single forward-pass timing + GPU memory estimate
    without full training.  Outputs:
      - forward pass time per batch (ms)
      - GPU VRAM used (if CUDA)
      - estimate of time to train 30 epochs on current dataset

Usage
-----
    # Benchmark only (fast, no training)
    python src/04d_train_player_context_minute.py --benchmark --limit 500

    # Quick smoke test (CPU, small K and sequence length)
    python src/04d_train_player_context_minute.py --limit 5000 --epochs 3 --k 5 --max-seq-len 15

    # Full run on GPU workstation
    python src/04d_train_player_context_minute.py --epochs 30 --k 50
"""

