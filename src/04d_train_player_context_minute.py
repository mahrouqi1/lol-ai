# ── Main ──────────────────────────────────────────────────────────────────────

def main() -> None:
    args = parse_args()
    torch.set_num_threads(N_THREADS)
    t0 = time.time()

    device  = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    use_amp = device.type == "cuda"
    log.info("Device: %s | AMP: %s", device, use_amp)

    # ── Load game features ────────────────────────────────────────────────────
    log.info("Loading %s ...", FEATURES_PATH)
    df = pd.read_parquet(FEATURES_PATH)
    log.info("Loaded: %d rows x %d cols", len(df), df.shape[1])

    all_games = df["game_id"].unique()
    if args.limit:
        rng = np.random.default_rng(42)
        all_games = rng.choice(all_games, size=min(args.limit, len(all_games)), replace=False)
        df = df[df["game_id"].isin(all_games)]
        log.info("Limited to %d games -> %d rows", len(all_games), len(df))

    rng = np.random.default_rng(42)
    rng.shuffle(all_games)
    n = len(all_games)
    n_tr  = int(n * 0.80)
    n_val = int(n * 0.10)
    train_games = list(all_games[:n_tr])
    val_games   = list(all_games[n_tr: n_tr + n_val])

    feature_cols = [c for c in df.columns if c not in METADATA_COLS]
    cat_vocab    = build_cat_vocab(df, feature_cols)
    cont_cols, cat_groups = split_cont_cat(feature_cols, cat_vocab)

    train_mask  = df["game_id"].isin(set(train_games))
    cont_scaler = StandardScaler()
    cont_scaler.fit(df.loc[train_mask, cont_cols].values.astype(np.float32))
    log.info("Cont cols: %d | Cat groups: %d", len(cont_cols), len(cat_groups))

    # ── Load player summary (for timestamps + slot mapping) ───────────────────
    if not SUMMARY_PATH.exists():
        log.error("Player summary not found: %s — run 03b first", SUMMARY_PATH)
        sys.exit(1)
    log.info("Loading player summary ...")
    summary_df = pd.read_parquet(SUMMARY_PATH)

    # Build slot_puuids mapping
    slot_puuids: dict[str, dict[str, str]] = {}
    for row in summary_df.itertuples(index=False):
        gid  = row.game_id
        key  = (int(row.team_id), str(row.team_position).upper())
        slot = _POS_MAP.get(key)
        if slot is None:
            continue
        if gid not in slot_puuids:
            slot_puuids[gid] = {}
        slot_puuids[gid][slot] = row.puuid

    # ── Load player minute sequences ──────────────────────────────────────────
    seq_lookup:    dict = {}
    chrono_lookup: dict = {}

    if not args.no_sequences and SEQUENCES_PATH.exists():
        log.info("Loading minute sequences from %s ...", SEQUENCES_PATH)
        seq_df = pd.read_parquet(SEQUENCES_PATH)
        log.info("Sequence rows: %d", len(seq_df))
        seq_lookup = build_seq_lookup(seq_df)
        del seq_df  # free RAM

        # Build chrono lookup: puuid -> [(gc_ms, gid), ...] sorted asc
        log.info("Building chronological lookup ...")
        tmp: dict[str, list] = {}
        for row in summary_df.itertuples(index=False):
            p = row.puuid
            if p not in tmp:
                tmp[p] = []
            tmp[p].append((int(row.game_creation_ms), row.game_id))
        for p in tmp:
            tmp[p].sort()
        chrono_lookup = tmp
    else:
        if args.no_sequences:
            log.info("--no-sequences flag: using zero history (architecture test).")
        else:
            log.warning("Sequence file not found: %s", SEQUENCES_PATH)
            log.warning("Running with zero history.  Use 03b --include-sequences to generate it.")

        # Still build chrono from summary (needed for the lookup contract)
        for row in summary_df.itertuples(index=False):
            p = row.puuid
            if p not in chrono_lookup:
                chrono_lookup[p] = []
            chrono_lookup[p].append((int(row.game_creation_ms), row.game_id))
        for p in chrono_lookup:
            chrono_lookup[p].sort()

    # ── Build datasets ────────────────────────────────────────────────────────
    common_kw = dict(
        df=df,
        cont_cols=cont_cols,
        cat_groups=cat_groups,
        cont_scaler=cont_scaler,
        cat_vocab=cat_vocab,
        summary_df=summary_df,
        seq_lookup=seq_lookup,
        slot_puuids=slot_puuids,
        K=args.k,
        max_seq_len=args.max_seq_len,
        chrono_lookup=chrono_lookup,
    )
    log.info("Building datasets: train=%d val=%d", len(train_games), len(val_games))
    train_ds = MinuteLevelDataset(game_ids=train_games, **common_kw)
    val_ds   = MinuteLevelDataset(game_ids=val_games,   **common_kw)

    dl_kw = dict(
        batch_size=args.batch, collate_fn=collate_fn,
        num_workers=args.workers, pin_memory=(device.type == "cuda"),
    )
    train_dl = torch.utils.data.DataLoader(train_ds, shuffle=True,  **dl_kw)
    val_dl   = torch.utils.data.DataLoader(val_ds,   shuffle=False, **dl_kw)

    # ── Instantiate model ─────────────────────────────────────────────────────
    model = PlayerContextMinuteTransformer(
        n_cont       = len(cont_cols),
        cat_vocab    = cat_vocab,
        cat_embed_dim= 8,
        enc_d        = args.enc_d,
        player_d     = args.player_d,
        game_d       = args.game_d,
        nhead_game   = args.nhead_game,
        layers_game  = args.layers_game,
        ffn_game     = args.ffn_game,
        nhead_hist   = args.nhead_hist,
        layers_hist  = args.layers_hist,
        dropout      = args.dropout,
    ).to(device)

    if device.type == "cuda":
        try:
            model = torch.compile(model)
            log.info("torch.compile() applied.")
        except Exception:
            pass

    n_params = sum(p.numel() for p in model.parameters() if p.requires_grad)
    log.info("Model params: %s  enc_d=%d player_d=%d game_d=%d",
             f"{n_params:,}", args.enc_d, args.player_d, args.game_d)
    log.info("History: K=%d games x max_seq_len=%d frames", args.k, args.max_seq_len)

    # ── Benchmark mode ────────────────────────────────────────────────────────
    if args.benchmark:
        run_benchmark(model, train_dl, device, use_amp, n_batches=10)
        log.info("Benchmark complete. Exiting.")
        return

    # ── Training ──────────────────────────────────────────────────────────────
    optimizer  = torch.optim.AdamW(model.parameters(), lr=args.lr, weight_decay=1e-4)
    scheduler  = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=args.epochs)
    scaler_amp = torch.amp.GradScaler("cuda", enabled=use_amp)

    log.info("Starting training: max %d epochs, patience %d", args.epochs, args.patience)
    log.info("Log: loss=train/val | AUC@5m=tr/val  AUC@10m=tr/val  AUC@end=tr/val")

    best_val_auc = 0.0
    best_state   = None
    patience_cnt = 0

    for epoch in range(1, args.epochs + 1):
        model.train()
        ep_t = time.time()
        total_loss = 0.0
        n_batches  = 0

        for batch in train_dl:
            x_cont     = batch["x_cont"].to(device)
            x_cat      = {s: v.to(device) for s, v in batch["x_cat"].items()}
            minutes    = batch["minutes"].to(device)
            lengths    = batch["lengths"].to(device)
            labels     = batch["labels"].to(device)
            hist_seqs  = batch["hist_seqs"].to(device)
            hist_mins  = batch["hist_mins"].to(device)
            hist_glen  = batch["hist_glen"].to(device)
            hist_kmask = batch["hist_kmask"].to(device)

            B, T = labels.shape
            real_mask = torch.arange(T, device=device).unsqueeze(0) < lengths.unsqueeze(1)

            smooth = labels.clone()
            smooth[real_mask & (labels == 1)] = 1.0 - args.label_smooth
            smooth[real_mask & (labels == 0)] = args.label_smooth

            with torch.amp.autocast(device_type=device.type, enabled=use_amp, dtype=torch.bfloat16):
                probs = model(
                    x_cont, x_cat, minutes, lengths,
                    hist_seqs, hist_mins, hist_glen, hist_kmask,
                )
            probs = probs.float().nan_to_num(nan=0.5)
            loss = F.binary_cross_entropy(probs[real_mask].clamp(1e-6, 1 - 1e-6), smooth[real_mask].float())

            scaler_amp.scale(loss).backward()
            scaler_amp.unscale_(optimizer)
            nn.utils.clip_grad_norm_(model.parameters(), max_norm=5.0)
            scaler_amp.step(optimizer)
            scaler_amp.update()
            optimizer.zero_grad(set_to_none=True)

            total_loss += loss.item()
            n_batches  += 1

        scheduler.step()
        train_loss = total_loss / max(n_batches, 1)

        val_aucs   = eval_auc(model, val_dl,   device, use_amp)
        train_aucs = eval_auc(model, train_dl, device, use_amp)

        ep_s = time.time() - ep_t
        log.info(
            "Epoch %02d/%02d | loss=%.4f | "
            "AUC@5m=%.3f/%.3f  AUC@10m=%.3f/%.3f  AUC@end=%.3f/%.3f | %.1fs",
            epoch, args.epochs, train_loss,
            train_aucs["5m"], val_aucs["5m"],
            train_aucs["10m"], val_aucs["10m"],
            train_aucs["end"], val_aucs["end"],
            ep_s,
        )

        if val_aucs["end"] > best_val_auc:
            best_val_auc = val_aucs["end"]
            best_state   = {k: v.cpu().clone() for k, v in model.state_dict().items()}
            patience_cnt = 0
        else:
            patience_cnt += 1
            if patience_cnt >= args.patience:
                log.info("Early stopping at epoch %d.", epoch)
                break

    log.info("Restoring best weights (val AUC@end=%.4f).", best_val_auc)
    if best_state is not None:
        model.load_state_dict(best_state)

    torch.save(model.state_dict(), MODEL_OUT)
    log.info("Saved model: %s", MODEL_OUT)

    with open(ARTIFACT_OUT, "wb") as f:
        pickle.dump({
            "cont_scaler": cont_scaler, "cat_vocab": cat_vocab,
            "cont_cols": cont_cols, "cat_groups": cat_groups,
            "K": args.k, "max_seq_len": args.max_seq_len,
            "best_val_auc": best_val_auc,
        }, f)
    log.info("Saved artifacts: %s", ARTIFACT_OUT)

    elapsed = time.time() - t0
    log.info("Done in %.1f s (%.1f min)", elapsed, elapsed / 60)
    print(f"\nBest val AUC@end: {best_val_auc:.4f}")


if __name__ == "__main__":
    main()
